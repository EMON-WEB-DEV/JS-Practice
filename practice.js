const PI = 3.14159;
let radius = 5;
let area = PI * radius * radius;


console.log("Area of the circle:", area);
console.log("Radius:", radius);
radius = 10;
area = PI * radius * radius;
console.log("New area of the circle:", area);
console.log("New radius:", radius);

// This code calculates the area of a circle given its radius.
// It demonstrates the use of const, let, and var for variable declarations.
// The area is recalculated after changing the radius.

// Output:
// Area of the circle: 78.53975
// Radius: 5
// New area of the circle: 314.159
// New radius: 10




